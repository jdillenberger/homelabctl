// DeployedApp holds information about a deployed app instance.
type DeployedApp struct {
	Name       string            `json:"name"`
	Template   string            `json:"template"`
	Values     map[string]string `json:"values"`
	DeployedAt time.Time         `json:"deployed_at"`
	Version    string            `json:"version"`
}

// DeployOptions holds parameters for a deploy operation.
type DeployOptions struct {
	Values  map[string]string
	DryRun  bool
	Confirm bool // if true, skip confirmation prompt
}

// Manager handles app lifecycle operations.
type Manager struct {
	cfg      *config.Config
	registry *Registry
	renderer *TemplateRenderer
	compose  *Compose
	runner   *exec.Runner
}

// NewManager creates a new app manager.
func NewManager(cfg *config.Config, runner *exec.Runner, tmplFS fs.FS) (*Manager, error) {
	registry, err := NewRegistry(tmplFS)
	if err != nil {
		return nil, fmt.Errorf("loading registry: %w", err)
	}

	return &Manager{
		cfg:      cfg,
		registry: registry,
		renderer: NewTemplateRenderer(registry),
		compose:  NewCompose(runner, cfg.Docker.ComposeCommand),
		runner:   runner,
	}, nil
}

// Registry returns the app template registry.
func (m *Manager) Registry() *Registry {
	return m.registry
}

// ensureNetwork creates the Docker network if it does not already exist.
func (m *Manager) ensureNetwork() error {
	network := m.cfg.Docker.DefaultNetwork
	// Try to create; if it already exists the error is harmless.
	if _, err := m.runner.Run("docker", "network", "create", network); err != nil {
		// Verify the network actually exists (it may have been created concurrently).
		if _, inspectErr := m.runner.Run("docker", "network", "inspect", network); inspectErr != nil {
			return fmt.Errorf("ensuring docker network %s: %w", network, err)
		}
	}
	return nil
}

// acquireLock takes an exclusive file lock to prevent concurrent operations on the same app.
// Returns the lock file which must be passed to releaseLock when done.
func (m *Manager) acquireLock(appName string) (*os.File, error) {
	lockDir := filepath.Join(m.cfg.AppsDir, ".locks")
	if err := os.MkdirAll(lockDir, 0o755); err != nil {
		return nil, fmt.Errorf("creating lock directory: %w", err)
	}
	lockPath := filepath.Join(lockDir, appName+".lock")
	f, err := os.OpenFile(lockPath, os.O_CREATE|os.O_RDWR, 0o600)
	if err != nil {
		return nil, fmt.Errorf("opening lock file: %w", err)
	}
	if err := syscall.Flock(int(f.Fd()), syscall.LOCK_EX|syscall.LOCK_NB); err != nil {
		f.Close()
		return nil, fmt.Errorf("app %s: another operation is in progress", appName)
	}
	return f, nil
}

// releaseLock releases the file lock and removes the lock file.
func (m *Manager) releaseLock(f *os.File) {
	_ = syscall.Flock(int(f.Fd()), syscall.LOCK_UN)
	name := f.Name()
	f.Close()
	os.Remove(name)
}

// Deploy deploys an app from its template with the given values.
func (m *Manager) Deploy(appName string, opts DeployOptions) error {
	lock, err := m.acquireLock(appName)
	if err != nil {
		return err
	}
	defer m.releaseLock(lock)

	meta, ok := m.registry.Get(appName)
	if !ok {
		return fmt.Errorf("unknown app template: %s", appName)
	}

	// Check if already deployed
	appDir := m.cfg.AppDir(appName)
	if _, err := os.Stat(filepath.Join(appDir, ".homelabctl.json")); err == nil {
		return fmt.Errorf("app %s is already deployed (remove first)", appName)
	}

	// Merge defaults into values
	mergedValues := make(map[string]string)
	for _, v := range meta.Values {
		if v.Default != "" {
			mergedValues[v.Name] = v.Default
		}
	}
	for k, v := range opts.Values {
		mergedValues[k] = v
	}

	// Auto-generate secrets
	autoGenerated := make(map[string]bool)
	for _, v := range meta.Values {
		if v.AutoGen != "" && mergedValues[v.Name] == "" {
			switch v.AutoGen {
			case "password":
				pw, err := genPassword()
				if err != nil {
					return fmt.Errorf("generating password for %s: %w", v.Name, err)
				}
				mergedValues[v.Name] = pw
				autoGenerated[v.Name] = true
			case "uuid":
				mergedValues[v.Name] = uuid.New().String()
				autoGenerated[v.Name] = true
			}
		}
	}

	// Ensure timezone has a default even if not declared in app.yaml
	if mergedValues["timezone"] == "" {
		mergedValues["timezone"] = "UTC"
	}

	// Validate required values
	for _, v := range meta.Values {
		if v.Required && mergedValues[v.Name] == "" {
			return fmt.Errorf("required value %q is not set and has no default", v.Name)
		}
	}

	// Add standard values
	mergedValues["hostname"] = m.cfg.Hostname
	mergedValues["domain"] = m.cfg.Network.Domain
	mergedValues["data_dir"] = m.cfg.DataPath(appName)
	mergedValues["app_name"] = appName
	mergedValues["network"] = m.cfg.Docker.DefaultNetwork

	// Render templates
	rendered, err := m.renderer.RenderAllFiles(appName, mergedValues)
	if err != nil {
		return fmt.Errorf("rendering templates: %w", err)
	}

	// Get static files
	staticFiles, err := m.renderer.CopyStaticFiles(appName)
	if err != nil {
		return fmt.Errorf("reading static files: %w", err)
	}

	if opts.DryRun {
		fmt.Println("Dry run — files that would be written:")
		for name, content := range rendered {
			fmt.Printf("\n--- %s ---\n%s", name, content)
		}
		for name := range staticFiles {
			fmt.Printf("\n--- %s (static) ---\n", name)
		}
		return nil
	}

	// Show confirmation summary unless --yes was passed
	if !opts.Confirm {
		printDeploySummary(appName, meta, mergedValues, autoGenerated)
		if !AskConfirmation("Proceed with deploy?") {
			return fmt.Errorf("deploy cancelled by user")
		}
	}

	// Ensure docker network exists
	if err := m.ensureNetwork(); err != nil {
		return err
	}

	// Create directories
	if err := os.MkdirAll(appDir, 0o755); err != nil {
		return fmt.Errorf("creating app directory: %w", err)
	}
	dataDir := m.cfg.DataPath(appName)
	if err := os.MkdirAll(dataDir, 0o755); err != nil {
		return fmt.Errorf("creating data directory: %w", err)
	}

	// Write rendered files
	for name, content := range rendered {
		outPath := filepath.Join(appDir, name)
		if err := os.MkdirAll(filepath.Dir(outPath), 0o755); err != nil {
			return fmt.Errorf("creating directory for %s: %w", name, err)
		}
		if err := os.WriteFile(outPath, []byte(content), 0o600); err != nil {
			return fmt.Errorf("writing %s: %w", name, err)
		}
	}

	// Write static files
	for name, data := range staticFiles {
		outPath := filepath.Join(appDir, name)
		if err := os.MkdirAll(filepath.Dir(outPath), 0o755); err != nil {
			return fmt.Errorf("creating directory for %s: %w", name, err)
		}
		if err := os.WriteFile(outPath, data, 0o600); err != nil {
			return fmt.Errorf("writing %s: %w", name, err)
		}
	}

	// Save deployment info
	deployed := &DeployedApp{
		Name:       appName,
		Template:   appName,
		Values:     mergedValues,
		DeployedAt: time.Now(),
		Version:    meta.Version,
	}
	infoData, err := json.MarshalIndent(deployed, "", "  ")
	if err != nil {
		return fmt.Errorf("serializing deploy info: %w", err)
	}
	if err := os.WriteFile(filepath.Join(appDir, ".homelabctl.json"), infoData, 0o600); err != nil {
		return fmt.Errorf("writing deploy info: %w", err)
	}

	// Run docker compose up
	fmt.Printf("Starting %s...\n", appName)
	if _, err := m.compose.Up(appDir); err != nil {
		// Clean up on failure so the user can retry without needing to remove first.
		_, _ = m.compose.Down(appDir)
		os.RemoveAll(appDir)
		return fmt.Errorf("starting containers: %w", err)
	}

	// Print access information
	hostname := mergedValues["hostname"]
	domain := mergedValues["domain"]
	fqdn := hostname + "." + domain

	fmt.Printf("\nApp %s deployed successfully.\n\n", appName)
	for _, p := range meta.Ports {
		if p.ValueName != "" {
			if portVal, ok := mergedValues[p.ValueName]; ok {
				scheme := "http"
				if strings.Contains(strings.ToLower(p.Description), "https") {
					scheme = "https"
				}
				fmt.Printf("  %-20s %s://%s:%s\n", p.Description+":", scheme, fqdn, portVal)
			}
		} else {
			fmt.Printf("  %-20s :%d (container :%d)\n", p.Description+":", p.Host, p.Container)
		}
	}
	fmt.Printf("  %-20s %s\n", "Data:", m.cfg.DataPath(appName))
	fmt.Println()

	// Show post-deploy info if available
	if meta.PostDeployInfo != nil {
		printPostDeployInfo(meta.PostDeployInfo, mergedValues)
	}

	// Execute post-deploy hooks
	if meta.Hooks != nil {
		executeHooks(meta.Hooks.PostDeploy, mergedValues, m.runner)
	}

	return nil
}

// printDeploySummary shows what will be deployed before asking for confirmation.
func printDeploySummary(appName string, meta *AppMeta, mergedValues map[string]string, autoGenerated map[string]bool) {
	fmt.Printf("\n=== Deploy Summary ===\n\n")
	fmt.Printf("  App:      %s\n", appName)
	fmt.Printf("  Version:  %s\n", meta.Version)
	fmt.Println()

	// Port mappings
	if len(meta.Ports) > 0 {
		fmt.Println("  Ports:")
		for _, p := range meta.Ports {
			hostPort := fmt.Sprintf("%d", p.Host)
			if p.ValueName != "" {
				if v, ok := mergedValues[p.ValueName]; ok {
					hostPort = v
				}
			}
			fmt.Printf("    %s:%d/%s  (%s)\n", hostPort, p.Container, p.Protocol, p.Description)
		}
		fmt.Println()
	}

	// Volume mounts
	if len(meta.Volumes) > 0 {
		fmt.Println("  Volumes:")
		dataDir := mergedValues["data_dir"]
		for _, v := range meta.Volumes {
			fmt.Printf("    %s/%s -> %s  (%s)\n", dataDir, v.Name, v.Container, v.Description)
		}
		fmt.Println()
	}

	// Auto-generated secrets
	if len(autoGenerated) > 0 {
		fmt.Println("  Auto-generated secrets:")
		for name := range autoGenerated {
			fmt.Printf("    %s  [generated]\n", name)
		}
		fmt.Println()
	}
}

// AskConfirmation prompts the user for y/n and returns true if they confirm.
func AskConfirmation(prompt string) bool {
	fmt.Printf("%s [y/N]: ", prompt)
	scanner := bufio.NewScanner(os.Stdin)
	if scanner.Scan() {
		answer := strings.TrimSpace(strings.ToLower(scanner.Text()))
		return answer == "y" || answer == "yes"
	}
	return false
}

// Remove removes a deployed app.
func (m *Manager) Remove(appName string, keepData bool) error {
	lock, err := m.acquireLock(appName)
	if err != nil {
		return err
	}
	defer m.releaseLock(lock)

	appDir := m.cfg.AppDir(appName)
	if _, err := os.Stat(appDir); os.IsNotExist(err) {
		return fmt.Errorf("app %s is not deployed", appName)
	}

	// Execute pre-remove hooks
	meta, ok := m.registry.Get(appName)
	if ok && meta.Hooks != nil {
		executeHooks(meta.Hooks.PreRemove, map[string]string{
			"app_name": appName,
			"hostname": m.cfg.Hostname,
			"domain":   m.cfg.Network.Domain,
			"data_dir": m.cfg.DataPath(appName),
		}, m.runner)
	}

	// Stop containers
	fmt.Printf("Stopping %s...\n", appName)
	if _, err := m.compose.Down(appDir); err != nil {
		fmt.Printf("Warning: failed to stop containers: %v\n", err)
	}

	// Remove app directory
	if err := os.RemoveAll(appDir); err != nil {
		return fmt.Errorf("removing app directory: %w", err)
	}

	if !keepData {
		dataDir := m.cfg.DataPath(appName)
		if err := os.RemoveAll(dataDir); err != nil {
			// Containers often create root-owned files. Clean contents via Docker, then remove the dir.
			if _, dockerErr := m.runner.Run("docker", "run", "--rm",
				"-v", dataDir+":/data", "alpine", "sh", "-c", "rm -rf /data/*"); dockerErr != nil {
				return fmt.Errorf("removing data directory: %w (docker fallback also failed: %v)", err, dockerErr)
			}
			os.RemoveAll(dataDir)
		}
		fmt.Printf("App %s removed (including data).\n", appName)
	} else {
		fmt.Printf("App %s removed (data kept at %s).\n", appName, m.cfg.DataPath(appName))
	}

	return nil
}

// Start starts a deployed app's containers.
func (m *Manager) Start(appName string) error {
	appDir := m.cfg.AppDir(appName)
	if _, err := os.Stat(appDir); os.IsNotExist(err) {
		return fmt.Errorf("app %s is not deployed", appName)
	}
	_, err := m.compose.Start(appDir)
	return err
}

