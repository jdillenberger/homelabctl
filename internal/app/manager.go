// Status returns the container status for an app.
func (m *Manager) Status(appName string) (string, error) {
	appDir := m.cfg.AppDir(appName)
	if _, err := os.Stat(appDir); os.IsNotExist(err) {
		return "", fmt.Errorf("app %s is not deployed", appName)
	}
	result, err := m.compose.PS(appDir)
	if err != nil {
		return "", err
	}
	return result.Stdout, nil
}

// ListDeployed returns the names of all deployed apps.
func (m *Manager) ListDeployed() ([]string, error) {
	entries, err := os.ReadDir(m.cfg.AppsDir)
	if err != nil {
		if os.IsNotExist(err) {
			return nil, nil
		}
		return nil, err
	}

	var apps []string
	for _, entry := range entries {
		if !entry.IsDir() {
			continue
		}
		infoPath := filepath.Join(m.cfg.AppsDir, entry.Name(), ".homelabctl.json")
		if _, err := os.Stat(infoPath); err == nil {
			apps = append(apps, entry.Name())
		}
	}
	return apps, nil
}

// GetDeployedInfo reads the deployment info for an app.
func (m *Manager) GetDeployedInfo(appName string) (*DeployedApp, error) {
	infoPath := filepath.Join(m.cfg.AppDir(appName), ".homelabctl.json")
	data, err := os.ReadFile(infoPath)
	if err != nil {
		return nil, fmt.Errorf("reading deploy info: %w", err)
	}
	var info DeployedApp
	if err := json.Unmarshal(data, &info); err != nil {
		return nil, fmt.Errorf("parsing deploy info: %w", err)
	}
	return &info, nil
}

// SaveDeployedInfo writes updated deployment info for an app.
func (m *Manager) SaveDeployedInfo(appName string, info *DeployedApp) error {
	infoPath := filepath.Join(m.cfg.AppDir(appName), ".homelabctl.json")
	data, err := json.MarshalIndent(info, "", "  ")
	if err != nil {
		return fmt.Errorf("serializing deploy info: %w", err)
	}
	return os.WriteFile(infoPath, data, 0o600)
}

// printPostDeployInfo displays post-deploy information to the user.
func printPostDeployInfo(info *PostDeployInfo, values map[string]string) {
	fmt.Println("--- Post-Deploy Info ---")

	if info.AccessURL != "" {
		if rendered, err := renderTemplate(info.AccessURL, values); err == nil {
			fmt.Printf("  Access URL:    %s\n", rendered)
		}
	}

	if info.Credentials != "" {
		if rendered, err := renderTemplate(info.Credentials, values); err == nil {
			fmt.Printf("  Credentials:   %s\n", rendered)
		}
	}

	if len(info.Notes) > 0 {
		fmt.Println("  First-time setup:")
		for i, note := range info.Notes {
			if rendered, err := renderTemplate(note, values); err == nil {
				fmt.Printf("    %d. %s\n", i+1, rendered)
			}
		}
	}

	fmt.Println()
}

// renderTemplate renders a Go template string with the given values.
func renderTemplate(tmplStr string, values map[string]string) (string, error) {
	t, err := template.New("").Parse(tmplStr)
	if err != nil {
		return tmplStr, err
	}
	var buf bytes.Buffer
	if err := t.Execute(&buf, values); err != nil {
		return tmplStr, err
	}
	return buf.String(), nil
}

// executeHooks runs lifecycle hooks, logging errors but not failing the deploy.
func executeHooks(hooks []Hook, values map[string]string, runner *exec.Runner) {
	for _, hook := range hooks {
		switch hook.Type {
		case "exec":
			cmd, err := renderTemplate(hook.Command, values)
			if err != nil {
				slog.Warn("Hook: failed to render command template", "error", err)
				continue
			}
			slog.Info("Running post-deploy hook", "command", cmd)
			if _, err := runner.Run("sh", "-c", cmd); err != nil {
				slog.Warn("Post-deploy hook failed", "command", cmd, "error", err)
			}
		case "http":
			url, err := renderTemplate(hook.URL, values)
			if err != nil {
				slog.Warn("Hook: failed to render URL template", "error", err)
				continue
			}
			method := hook.Method
			if method == "" {
				method = "GET"
			}
			var bodyReader *bytes.Reader
			if hook.Body != "" {
				body, err := renderTemplate(hook.Body, values)
				if err != nil {
					slog.Warn("Hook: failed to render body template", "error", err)
					continue
				}
				bodyReader = bytes.NewReader([]byte(body))
			} else {
				bodyReader = bytes.NewReader(nil)
			}
			slog.Info("Running post-deploy HTTP hook", "method", method, "url", url)
			req, err := http.NewRequest(method, url, bodyReader)
			if err != nil {
				slog.Warn("Hook: failed to create request", "error", err)
				continue
			}
			if hook.Body != "" {
				req.Header.Set("Content-Type", "application/json")
			}
			client := &http.Client{Timeout: 30 * time.Second}
			resp, err := client.Do(req)
			if err != nil {
				slog.Warn("Post-deploy HTTP hook failed", "url", url, "error", err)
				continue
			}
			resp.Body.Close()
			if resp.StatusCode >= 400 {
				slog.Warn("Post-deploy HTTP hook returned error", "url", url, "status", resp.StatusCode)
			}
		}
	}
}
